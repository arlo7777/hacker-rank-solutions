def reverse(head):
    #have to reverse the direction of the pointers in this singly linked list
    #to accomplish this we need to initialize 3 pointers 
    previous = None
    current = head
    next_pointer = head.next
    
    while current != None:
        next_pointer = current.next
        
        #change the direction of the nodes
        current.next = previous
        
        #shifting the nodes
        previous = current 
        current = next_pointer
     
    #store the head
    head = previous
    
    #return the head
    return head
        
        
        

